package MultilevelInheritance;

public class college {
    String collegeName;
    static String collegeAddress;
    long collegePhone;
    String collegeEmail;
    String collegePlace;

public college(String collegeName, String collegeAddress, long collegePhone, String collegeEmail, String collegePlace){
    this.collegeName = collegeName;
    this.collegeAddress = collegeAddress;
    this.collegePhone = collegePhone;
    this.collegeEmail = collegeEmail;
    this.collegePlace = collegePlace;
    }
    
}
